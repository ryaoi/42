#include "ft_ls.h"

int			count_digit(int link)
{
	int		i;

	i = 1;
	while (link > 9)
	{
		link /= 10;
		i++;
	}
	return (i);
}

int			count_str(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
